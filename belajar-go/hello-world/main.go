package main

import "fmt"

func main() {
	fmt.Println("hello world")

	// penggunaan fmt.Print("")
	// perlu menambahkan spasi apabila memasukan beberapa nilai
	fmt.Print("hai", " ", "kamu\n")

	// pegunnan fmt.Println("")
	// apabila terdapat beberapa nilai di dalamnya otomatis dipisahkan dengan spasi
	fmt.Println("hai", "juga")
}
